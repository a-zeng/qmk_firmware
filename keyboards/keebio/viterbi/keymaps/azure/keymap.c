bool process_record_user(uint16_t keycode, keyrecord_t *record) {
  switch (keycode) {
    case QWERTY:
      if (record->event.pressed) {
        set_single_persistent_default_layer(_QWERTY);
      }
      return false;
      break;
    case LOWER:
      if (record->event.pressed) {
        layer_on(_LOWER);
        update_tri_layer(_LOWER, _RAISE, _ADJUST);
      } else {
        layer_off(_LOWER);
        update_tri_layer(_LOWER, _RAISE, _ADJUST);
      }
      return false;
      break;
    case RAISE:
      if (record->event.pressed) {
        layer_on(_RAISE);
        update_tri_layer(_LOWER, _RAISE, _ADJUST);
      } else {
        layer_off(_RAISE);
        update_tri_layer(_LOWER, _RAISE, _ADJUST);
      }
      return false;
      break;
    case ADJUST:
      if (record->event.pressed) {
        layer_on(_ADJUST);
      } else {
        layer_off(_ADJUST);
      }
      return false;
      break;
  }
  return true;
}

layer_state_t layer_state_set_user(layer_state_t state) {
    switch (get_highest_layer(state)) {
        case _RAISE:
            rgblight_setrgb(RGB_GREEN);
            // rgblight_setrgb_slave(RGB_GREEN);
            break;
        case _LOWER:
            rgblight_setrgb(RGB_RED);
            // rgblight_setrgb_slave(RGB_RED);
            break;
        case _ADJUST:
            rgblight_setrgb(RGB_PURPLE);
            // rgblight_setrgb_slave(RGB_PURPLE);
            break;
        default: // for any other layers, or the default layer
            rgblight_setrgb(RGB_CYAN);
            // rgblight_setrgb_slave(RGB_CYAN);
            break;
    } 
  return state;
}